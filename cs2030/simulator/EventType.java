package cs2030.simulator;

/**
 * EventType enum which holds the different types of events. There are seven types of events
 * possible: ARRIVES, SERVE, WAITS, LEAVES, DONE, SERVER_REST, SERVER_BACK.
 */
public enum EventType {
   ARRIVES,
   SERVE,
   WAITS,
   LEAVES,
   DONE,
   SERVER_REST,
   SERVER_BACK
}
